char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (dest[i] != '\0')
		i++;
	while (src[b] != '\0' && i < n)
	{
		dest[i + b] = src[b];
		b++;
	}
	dest[b + i] = '\0';
	return dest;
}
